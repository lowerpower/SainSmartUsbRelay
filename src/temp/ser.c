int
dump_hexbuffer(char *buffer, int len)
{
    int i,j;

    i=j=0;
    while(i+j<len)
    {
        j+=i;
        printf("0x%04x ",j);
        for(i=0;(i<16) && ((i+j)<len);i++)
        {
            printf("%02x ",buffer[i+j]);
            if(8==i) printf(" ");
        }
        printf("  ");
        for(i=0;(i<16) && ((i+j)<len);i++)
        {
            if((buffer[i+j]>=32) && (buffer[i+j]<128))
                printf("%c",buffer[i+j]);
            else
                printf(".");
        }
        printf("\n");
    }
    printf("\n");
    return(0);
}

int
set_interface_attribs (int fd, int speed, int parity)
{
        struct termios tty;
        if (tcgetattr (fd, &tty) != 0)
        {
                printf ("error %d from tcgetattr", errno);
                return -1;
        }

        cfsetospeed (&tty, speed);
        cfsetispeed (&tty, speed);

        tty.c_cflag = (tty.c_cflag & ~CSIZE) | CS8;     // 8-bit chars
        // disable IGNBRK for mismatched speed tests; otherwise receive break
        // as \000 chars
        tty.c_iflag &= ~IGNBRK;         // disable break processing
        tty.c_lflag = 0;                // no signaling chars, no echo,
                                        // no canonical processing
        tty.c_oflag = 0;                // no remapping, no delays
        tty.c_cc[VMIN]  = 0;            // read doesn't block
        tty.c_cc[VTIME] = 5;            // 0.5 seconds read timeout

        tty.c_iflag &= ~(IXON | IXOFF | IXANY); // shut off xon/xoff ctrl

        tty.c_cflag |= (CLOCAL | CREAD);// ignore modem controls,
                                        // enable reading
        tty.c_cflag &= ~(PARENB | PARODD);      // shut off parity
        tty.c_cflag |= parity;
        tty.c_cflag &= ~CSTOPB;
        tty.c_cflag &= ~CRTSCTS;

        if (tcsetattr (fd, TCSANOW, &tty) != 0)
        {
                printf ("error %d from tcsetattr", errno);
                return -1;
        }
        printf("OK set interface\n");
        return 0;
}

void
set_blocking (int fd, int should_block)
{
        struct termios tty;
        memset (&tty, 0, sizeof tty);
        if (tcgetattr (fd, &tty) != 0)
        {
                printf ("error %d from tggetattr", errno);
                return;
        }

        tty.c_cc[VMIN]  = should_block ? 1 : 0;
        tty.c_cc[VTIME] = 5;            // 0.5 seconds read timeout

        if (tcsetattr (fd, TCSANOW, &tty) != 0)
                printf ("error %d setting term attributes", errno);

       printf("OK set blocking\n");                
}

void
read_status(int fd)
{
	unsigned char buf[1024];
    int n;
    
    n = read (fd, buf, sizeof buf);  // read up to 100 characters if ready to read

    if(n>0)
    {
        printf("n=%d  ->%s\n",n,buf);
        dump_hexbuffer(buf, n);
    }    
}

